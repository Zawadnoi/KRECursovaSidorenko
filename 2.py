# 18.  Обчислити висоту hc і медіану mа.

a = ((18-0)**2+(17-0)**2)**0.5          # Сторона А
b = ((-18-18)**2+(19-17)**2)**0.5       # Сторона Б
c = ((0-(-18))**2+(19-17)**2)**0.5      # Сторона С


p = (a+b+c)/2                           # Полупериметр
hc = 2/c*(p*(p-a)*(p-b)*(p-c))**0.5     # Висота на сторону С
ma = 0.5*(2*b**2+2*c**2-a**2)**0.5      # Медіана на сторону А

print("Висота на сторону С:", hc, "\nМедіана на сторону А:", ma)
